"""
Predictions models: LossPrediction records generated by the rule-based engine (Phase 1)
and upgraded ML model (Phase 2).
"""

from django.db import models


class LossPrediction(models.Model):
    """
    A loss risk prediction for a batch at a specific stage.
    Generated by the rule-based engine (Phase 1) or ML model (Phase 2).
    """

    class RiskLabel(models.TextChoices):
        GREEN = 'GREEN', 'Low Risk'
        AMBER = 'AMBER', 'Medium Risk'
        RED   = 'RED',   'High Risk'

    class PredictionStage(models.TextChoices):
        STORAGE         = 'STORAGE',         'Cold Storage'
        TRANSIT_LEG1    = 'TRANSIT_LEG1',    'Transit — Leg 1'
        SELF_TRANSPORT  = 'SELF_TRANSPORT',  'Self-Transport (Last Mile)'
        MARKET          = 'MARKET',          'Market Stall'

    class Phase(models.TextChoices):
        RULE_BASED = 'RULE_BASED', 'Phase 1 — Rule-Based Threshold Scoring'
        ML_MODEL   = 'ML_MODEL',   'Phase 2 — Scikit-learn ML Model'

    batch           = models.ForeignKey('traceability.Batch', on_delete=models.CASCADE,
                                        related_name='predictions', null=True, blank=True)
    # For self-transport predictions (per order, not just per batch)
    order           = models.ForeignKey('distribution.Order', on_delete=models.SET_NULL,
                                        null=True, blank=True, related_name='predictions')

    prediction_stage = models.CharField(max_length=20, choices=PredictionStage.choices)
    risk_score       = models.IntegerField(help_text="0–100. 0–40=Green, 41–70=Amber, 71–100=Red")
    risk_label       = models.CharField(max_length=10, choices=RiskLabel.choices)
    confidence_pct   = models.IntegerField(default=70, help_text="Confidence in this prediction (0–100%)")
    phase            = models.CharField(max_length=15, choices=Phase.choices, default=Phase.RULE_BASED)

    # Key contributing factors (stored as JSON for flexibility)
    contributing_factors = models.JSONField(default=list,
        help_text="List of factor dicts: [{'factor': 'transit_time', 'value': 7.5, 'weight': 'HIGH'}]"
    )

    # Human-readable recommendation
    recommendation = models.TextField(blank=True)

    # Whether an alert notification was generated for this prediction
    alert_generated = models.BooleanField(default=False)

    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['-created_at']
        indexes = [models.Index(fields=['batch', 'prediction_stage', 'created_at'])]

    def __str__(self):
        return f"Prediction: {self.risk_label} ({self.risk_score}) — {self.prediction_stage}"

    @classmethod
    def get_risk_label_from_score(cls, score):
        if score <= 40:
            return cls.RiskLabel.GREEN
        elif score <= 70:
            return cls.RiskLabel.AMBER
        else:
            return cls.RiskLabel.RED
